## TRIGGERS DE INTEGRIDAD

# REGISTRO AUTOMÁTICO HISTORIAL_APUESTAS
DELIMITER $$
CREATE TRIGGER trg_after_apuesta_insert
AFTER INSERT ON APUESTA
FOR EACH ROW
BEGIN
    INSERT INTO HISTORIAL_APUESTA
        (ID_Usuario, ID_Apuesta, ID_Evento, ID_Mercado)
    VALUES
        (NEW.ID_Usuario, NEW.ID_Apuesta,
         NEW.ID_Evento, NEW.ID_Mercado);
END$$
DELIMITER ;

# ACTUALIZACIÓN DE SALDO EN REGISTRO DE TRANSACCIÓN
DROP TRIGGER IF EXISTS trg_actualizar_saldo;

DELIMITER $$
CREATE TRIGGER trg_actualizar_saldo
AFTER INSERT ON TRANSACCION
FOR EACH ROW
BEGIN
    IF NEW.Tipo_transaccion IN ('DEPOSITO', 'GANANCIA') THEN
        UPDATE SALDO_CUENTA
        SET Saldo_disponible = Saldo_disponible + NEW.Valor
        WHERE ID_Usuario = NEW.ID_Usuario;
    ELSEIF NEW.Tipo_transaccion IN ('RETIRO', 'APUESTA') THEN
        UPDATE SALDO_CUENTA
        SET Saldo_disponible = GREATEST(0, Saldo_disponible - NEW.Valor)
        WHERE ID_Usuario = NEW.ID_Usuario;
    ELSEIF NEW.Tipo_transaccion = 'AJUSTE' THEN
        UPDATE SALDO_CUENTA
        SET Saldo_disponible = GREATEST(0, Saldo_disponible + NEW.Valor)
        WHERE ID_Usuario = NEW.ID_Usuario;
    END IF;
END$$
DELIMITER ;

DROP TRIGGER IF EXISTS trg_after_apuesta_insert;

