CREATE TABLE PARTICIPANTES (
ID_Participantes INT AUTO_INCREMENT NOT NULL,
Nombre VARCHAR(20) NOT NULL, 
Tipo ENUM ("EQUIPO", "DEPORTISTA", "OTRO") NOT NULL,
	CONSTRAINT PK_Participantes PRIMARY KEY (ID_Participantes)
    );
    
CREATE TABLE EVENTO (
ID_Evento INT AUTO_INCREMENT NOT NULL, 
Deporte VARCHAR(50) NOT NULL,
Tipo_Evento ENUM ("PARTIDO", "TORNEO", "CARRERA", "COMBATE", "OTRO") NOT NULL,
Fecha DATE NOT NULL,
Liga_torneo VARCHAR(50),
Resultado VARCHAR(50), 
	CONSTRAINT PK_ID_Evento PRIMARY KEY (ID_Evento)
    );
    
CREATE TABLE MERCADO (
ID_Mercado INT AUTO_INCREMENT NOT NULL, 
ID_Evento INT NOT NULL, 
Tipo_mercado ENUM ("GANADOR", "TOTAL_ANOTACIONES", "HANDICAP", "AMBOS_ANOTAN",
					"RESULTADO_EXACTO", "OTRO") NOT NULL, 
Cuota_actual FLOAT (8,2) NOT NULL, 
	CONSTRAINT PK_ID_Mercado PRIMARY KEY (ID_Mercado),
    FOREIGN KEY (ID_Evento) REFERENCES EVENTO(ID_Evento)
    ON DELETE CASCADE ON UPDATE CASCADE );

CREATE TABLE APUESTA (
ID_Apuesta INT AUTO_INCREMENT NOT NULL,
ID_Usuario INT NOT NULL,
ID_Evento INT NOT NULL,
ID_Mercado INT NOT NULL, 
Valor_apostado FLOAT(15,2) NOT NULL CHECK (Valor_apostado > 0), 
	CONSTRAINT PK_ID_Apuesta PRIMARY KEY (ID_Apuesta),
    FOREIGN KEY (ID_Usuario) REFERENCES USUARIO (ID_Usuario),
    FOREIGN KEY (ID_Evento) REFERENCES EVENTO (ID_Evento), 
    FOREIGN KEY (ID_Mercado) REFERENCES MERCADO (ID_Mercado)
    );
    
